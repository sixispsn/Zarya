# Таблица 9 СП 32.13330.2018: коэффициент γ для пересчёта q20 при P > 1
# Ключ: период P (лет), значение: множитель γ
GAMMA_PERIOD: dict[int, float] = {
    1: 1.00,
    2: 1.20,
    3: 1.38,
    5: 1.59,
    10: 1.87,
}


def get_city(code: str) -> StormCity | None:
    """Получить город по коду."""
    return STORM_CITIES.get(code)


def list_cities() -> list[StormCity]:
    """Список всех городов."""
    return list(STORM_CITIES.values())


def get_gamma(period: int) -> float:
    """Получить γ для заданного периода P (лет)."""
    if period not in GAMMA_PERIOD:
        raise ValueError(
            f"Период P={period} не поддерживается. "
            f"Допустимые значения: {list(GAMMA_PERIOD.keys())}"
        )
    return GAMMA_PERIOD[period]