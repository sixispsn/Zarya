def interp_ql(dn_mm: float, t_c: float) -> float:
    """
    Двумерная интерполяция qL по таблице 4 (диаметр × температура).
    Реализация 1-в-1 с interpQL() из HTML.
    """
    dns, ts, qs = T4_DN, T4_TEMP, T4_Q

    # Индекс DN
    di1, di2 = 0, 1
    for i in range(len(dns) - 1):
        if dns[i] <= dn_mm <= dns[i + 1]:
            di1, di2 = i, i + 1
            break
    else:
        if dn_mm >= dns[-1]:
            di1 = di2 = len(dns) - 1
        elif dn_mm <= dns[0]:
            di1 = di2 = 0

    # Индекс T
    ti1, ti2 = 0, 1
    for i in range(len(ts) - 1):
        if ts[i] <= t_c <= ts[i + 1]:
            ti1, ti2 = i, i + 1
            break
    else:
        if t_c >= ts[-1]:
            ti1 = ti2 = len(ts) - 1
        elif t_c <= ts[0]:
            ti1 = ti2 = 0

    kd = 0 if di1 == di2 else (dn_mm - dns[di1]) / (dns[di2] - dns[di1])
    kt = 0 if ti1 == ti2 else (t_c - ts[ti1]) / (ts[ti2] - ts[ti1])

    q00, q01 = qs[di1][ti1], qs[di1][ti2]
    q10, q11 = qs[di2][ti1], qs[di2][ti2]
    return q00 * (1 - kd) * (1 - kt) + q01 * (1 - kd) * kt + q10 * kd * (1 - kt) + q11 * kd * kt


def get_rnl(dn_mm: float, t_c: float) -> float:
    """
    Получить RнL по таблице В.3 с интерполяцией по DN.
    Реализация 1-в-1 с getRnL() из HTML.
    """
    tbl = RNL_TABLE
    i1, i2 = 0, 1
    for i in range(len(tbl) - 1):
        if tbl[i][0] <= dn_mm <= tbl[i + 1][0]:
            i1, i2 = i, i + 1
            break
    else:
        if dn_mm >= tbl[-1][0]:
            i1 = i2 = len(tbl) - 1

    k = 0 if i1 == i2 else (dn_mm - tbl[i1][0]) / (tbl[i2][0] - tbl[i1][0])

    # Колонка по температуре: 1=100°С, 2=300°С, 3=500°С
    ti = 1
    if t_c >= 300:
        ti = 2
    if t_c >= 500:
        ti = 3

    r1, r2 = tbl[i1][ti], tbl[i2][ti]
    return r1 + k * (r2 - r1)


def interp_tv4(t_room: float, humidity: int) -> float:
    """
    Перепад температур из таблицы В.4 с интерполяцией по температуре помещения.
    Реализация 1-в-1 с interpTV4() из HTML.
    """
    temps = [10, 15, 20, 25, 30]
    t = max(10, min(30, t_room))

    t1, t2 = temps[0], temps[1]
    for i in range(len(temps) - 1):
        if temps[i] <= t <= temps[i + 1]:
            t1, t2 = temps[i], temps[i + 1]
            break

    k = (t - t1) / (t2 - t1) if t2 != t1 else 0
    dt1 = TV4[t1].get(humidity, 0)
    dt2 = TV4[t2].get(humidity, 0)
    return dt1 + k * (dt2 - dt1)


