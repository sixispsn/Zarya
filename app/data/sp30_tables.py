@dataclass(frozen=True)
class ConsumerNorm:
    """
    Норма водопотребления для одного типа потребителя (таблица А.2).

    Расходы:
      qu_tot, qu_h  — суточные нормы (средние сутки), л на потребителя в сутки
      q_hr_tot, q_hr_h — нормы в час наибольшего водопотребления, л на потребителя в час
      q0_tot, q0_c, q0_h — секундный расход прибора, л/с
      q0hr_tot, q0hr_c, q0hr_h — часовой расход прибора, л/ч

    Холодная вода вычисляется как разница:
      qu_c = qu_tot - qu_h
      q_hr_c = q_hr_tot - q_hr_h
    (q0_c — отдельная величина из таблицы, не вычисляется)
    """
    code: str
    label: str
    unit: str  # "житель", "место", "учащийся" и т.п.

    # Суточные нормы, л/сут на потребителя
    qu_tot: float
    qu_h: float

    # Часовые нормы (в час макс. водопотребления), л/ч на потребителя
    q_hr_tot: float
    q_hr_h: float

    # Секундный расход прибора, л/с
    q0_tot: float
    q0_c: float
    q0_h: float

    # Часовой расход прибора, л/ч
    q0hr_tot: float
    q0hr_c: float
    q0hr_h: float


