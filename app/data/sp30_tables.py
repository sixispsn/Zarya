# ============================================================
# ТАБЛИЦА Б.2 (приложение Б СП 30.13330.2020)
# Коэффициенты α в зависимости от значения N*P.
#
# Используется для расчёта секундного и часового расхода:
#   q = 5 × q0 × α
#
# где NP = N (число приборов) × P (вероятность действия прибора).
#
# Это сокращённая таблица (наиболее частые значения).
# Полная таблица содержит много значений; для промежуточных
# используется линейная интерполяция или ближайшее меньшее.
# ============================================================

# Таблица в формате: список кортежей (NP, alpha)
# Отсортирована по возрастанию NP.
ALPHA_TABLE: list[tuple[float, float]] = [
    (0.015, 0.200), (0.02, 0.202), (0.03, 0.207), (0.04, 0.213),
    (0.05, 0.219), (0.06, 0.225), (0.07, 0.231), (0.08, 0.237),
    (0.09, 0.243), (0.1, 0.249), (0.15, 0.279), (0.2, 0.307),
    (0.25, 0.334), (0.3, 0.360), (0.35, 0.386), (0.4, 0.411),
    (0.45, 0.436), (0.5, 0.460), (0.6, 0.508), (0.7, 0.555),
    (0.8, 0.601), (0.9, 0.646), (1.0, 0.691), (1.5, 0.904),
    (2.0, 1.106), (2.5, 1.299), (3.0, 1.487), (4.0, 1.848),
    (5.0, 2.197), (6.0, 2.534), (7.0, 2.863), (8.0, 3.187),
    (9.0, 3.505), (10.0, 3.820),
]


def get_alpha(np_value: float) -> float:
    """
    Получить коэффициент α для заданного значения NP по таблице Б.2.

    Для значений NP вне таблицы:
    - меньше минимального: возвращаем минимальное значение α
    - больше максимального: возвращаем максимальное значение α
    
    Для промежуточных значений используется линейная интерполяция.

    Args:
        np_value: значение N*P (число приборов × вероятность действия)

    Returns:
        Коэффициент α (безразмерный)
    """
    if np_value <= ALPHA_TABLE[0][0]:
        return ALPHA_TABLE[0][1]
    if np_value >= ALPHA_TABLE[-1][0]:
        return ALPHA_TABLE[-1][1]

    # Линейная интерполяция между двумя ближайшими точками
    for i in range(len(ALPHA_TABLE) - 1):
        np1, a1 = ALPHA_TABLE[i]
        np2, a2 = ALPHA_TABLE[i + 1]
        if np1 <= np_value <= np2:
            # Линейная интерполяция
            return a1 + (a2 - a1) * (np_value - np1) / (np2 - np1)

    # До сюда не должно доходить, но на всякий случай
    return ALPHA_TABLE[-1][1]


def get_consumer_norm(code: str) -> Optional[ConsumerNorm]:
    """
    Получить норму водопотребления по коду группы потребителей.

    Args:
        code: код группы (например, "office", "residential_with_bath_centralized_hw")

    Returns:
        ConsumerNorm или None если код не найден.
    """
    return CONSUMER_NORMS.get(code)


def list_consumer_norms() -> list[ConsumerNorm]:
    """Получить список всех доступных групп потребителей."""
    return list(CONSUMER_NORMS.values())