def get_alpha(np_value: float) -> float:
    """
    Получить коэффициент α для заданного значения NP по таблице Б.2.

    Поведение:
    - NP <= 0: возвращаем минимум α = 0.200
    - NP < 0.015 (минимум таблицы): возвращаем α = 0.200
    - NP внутри таблицы: линейная интерполяция между соседними точками
    - NP > максимума таблицы: аппроксимация по формуле α = 1.72 × NP^0.49 - 0.5

    Реализация 1-в-1 с функцией getAlpha() из legacy HTML.
    """
    if np_value <= 0:
        return 0.200
    if np_value < ALPHA_TABLE[0][0]:
        return 0.200
    if np_value >= ALPHA_TABLE[-1][0]:
        # За пределами таблицы — аппроксимация
        return 1.72 * math.pow(np_value, 0.49) - 0.5

    # Линейная интерполяция между двумя ближайшими точками
    for i in range(len(ALPHA_TABLE) - 1):
        np1, a1 = ALPHA_TABLE[i]
        np2, a2 = ALPHA_TABLE[i + 1]
        if np1 <= np_value <= np2:
            k = (np_value - np1) / (np2 - np1)
            return a1 + k * (a2 - a1)

    return 0.200


